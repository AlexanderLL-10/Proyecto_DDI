    private boolean validarFormulario() {

        String nombre = jTextField_nombre.getText();
        if (nombre == null || "".equals(nombre)) {
            JOptionPane.showMessageDialog(this, "El nombre no puede estar vacio", "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        String apellidos = jTextField_apellidos.getText();
        if (apellidos == null || "".equals(apellidos)) {
            JOptionPane.showMessageDialog(this, "El apellido no puede estar vacio", "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        Date fecha = (Date)jSpinner_fecha.getValue();
        if (fecha == null || "".equals(fecha)) {
            JOptionPane.showMessageDialog(this, "La fecha no puede estar vacio", "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        
        String oficina = (String)jComboBox_oficina.getSelectedItem();

        if (oficina == null || "".equals(oficina)) {
            JOptionPane.showMessageDialog(this, "La oficina no puede estar vacio", "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }
        String mesa = (String)jComboBox_mesa.getSelectedItem();
        if (mesa == null || "".equals(mesa)) {
            JOptionPane.showMessageDialog(this, "La mesa no puede estar vacio", "Error", JOptionPane.ERROR_MESSAGE);
            return false;
        }

        return true;
    }
